package inheritance;

public class inheritancedocs {
	
	/*
	 * ---------inheritance--------
	 * 
	 * #code re-usability
	 * 
	 * #process of accessing properties and methods of parent/base/super class to child/sub/derived class is known as inheritance
	 * 
	 * #also known as IS-A relationship
	 * 
	 * #its a generalization to specialization process
	 * 
	 * #we can inherit only one class at a time
	 *  
	 * #multiple inheritance does not support in java with class but it happens with interface
	 *  
	 * #we can't inherit private properties, method and constructors
	 * 
	 * #'extends' keyword is used in inheritance 
	 * 
	 * 
	 * #syntax:
	 *  
	 *  child_class extends parent_class  {
	 *        
	 *       //properties    : parent + child
	 *       
	 *       //methods       : parent + child
	 *  
	 *  }
	 * 
	 * 
	 * 
	 * eg  
	 * 
	 * 
	 * class A{
	 * 
	 * }
	 * 
	 * child b extends A {
	 * 
	 * }
	 * 
	 * child C extends B  {
	 * 
	 * }	  
	 * 
	 * 
	 * 
	 */

}
