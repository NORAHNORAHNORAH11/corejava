package constructor;

public class constructordocs {
	
	/* 
	 * ---------constructor---------
	 *  
	 *  #USE TO CREATE OBJECT IN JAVA
	 *  #SPECIAL TYPE OF METHOD WHICH HAS SAME NAME AS CLASS NAME IS KNOWN AS CONSTRUCTOR 
	 *  #IF THERE IS NO ANY USER DEFINED CONSTRUCTOR IN CLASS THEN JAVA PROVIDES DEFAULT CONSTRUCTOR
	 *  #NO RETURN TYPE 
	 *  
	 *  #TYPES:
	 *  
	 *  A> DEFAULT CONSTRUCTOR 
	 *    -> USED TO CREAT OBJECT AND SET DEFAULT VALUES TO IT 
	 *       #SYNTAX
	 *         
	 *           class_name()  {
	 *           //set default values to object
	 *           }
	 * 
	 *  
	 *  B> PARAMETERIZED CONSTRUCTOR 
	 *    -> USE TO CREAT OBJECT AND SET DYNAMIC VALLUES TO IT 
	 *        #SYNTAX
	 *          
	 *            class_name(arg1, arg2,...., argn)  {
	 *              //set dynamic values to object
	 *              }
	 *            
	 */

}
