package abstraction;

public class Abstraction {
	
	/* 
	 * ----------Abstraction----------
	 * 
	 * # process of hiding implementation details in the program is known as abstraction.
	 * 
	 * # To achieve abstraction :
	 *  
	 *  a> Abstract class :
	 *    # Class with contains abstract (unimplemented) method and non-abstract methodis known as abstract class
	 *    
	 *    # Abstract method : 
	 *      -> method wihich does not have implementation details/body.
	 *      
	 *      e.g
	 *        abstract void print();
	 *        abstract int gertArea(int l, int b);
	 *        
	 *        
	 *      #every abstract class must have at least one child class and child class must override abstract method
	 *      
	 *      #we can't create object of abstract class
	 *      #level of abstraction 0-100%
	 *      
	 *      #syntax:  
	 *          
	 *            abstract class class_name{
	 *            
	 *               //abstract methods
	 *               
	 *               //non-abstract methods
	 *               
	 *               
	 *      
	 *   b> Interface :  
	 *     
	 *     #used to achieve abstraction 
	 *     #same as class which contains public abstract methods and public static final variable only 
	 *     
	 *     #every interface must have at least one implementation class and impl. class must override abstract methods 
	 *     
	 *     #we can't  create object of interface 
	 *     
	 *     #level of abstraction 100%
	 *     
	 *        #syntax :
	 *        
	 *          interface interface_name  {
	 *          
	 *            //public abstract methods 
	 *            
	 *           }
	 *           
	 *            
	 */

}
